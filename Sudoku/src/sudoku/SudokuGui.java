    private void runbanner() {
        // Timer for the banner to scroll text every 200 milliseconds
        Timer t = new Timer(200, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String s = jLabel1.getText();
                // This checks if the text has at least 2 characters to avoid error when it's empty or 1 character
                if (s.length() > 1) {
                    jLabel1.setText(s.substring(1) + s.charAt(0));  // Moves the first char to the end
                }
            }
        });
        t.start();  // Start the scrolling effect
    }

