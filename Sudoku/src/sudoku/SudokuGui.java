    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
       int gridSize = Integer.parseInt(jTextField1.getText()); // Take size from input box

        // Create Sudoku and generate puzzle
        Sudoku2 sudoku = new Sudoku2();
        solution = sudoku.getArray(gridSize);
        puzzle = sudoku.copyArray(solution);
        sudoku.setEmptyBlocks(puzzle);

        // Reset panel
        jPanel1.removeAll();
        jPanel1.setLayout(new GridLayout(gridSize, gridSize));

        // Initialize cells array to store references
        cells = new JTextField[gridSize][gridSize];

        for (int i = 0; i < gridSize; i++) {
            for (int j = 0; j < gridSize; j++) {
                JTextField cell = new JTextField(2);
                cell.setHorizontalAlignment(JTextField.CENTER);

                // Fill cell with non-empty puzzle values
                if (!puzzle[i][j].equals(" ")) {
                    cell.setText(puzzle[i][j]);
                    cell.setEditable(false);
                }

                cells[i][j] = cell; // Save reference for checking later
                jPanel1.add(cell);
            }
        }

        // Refresh panel
        jPanel1.revalidate();
        jPanel1.repaint();

    }//GEN-LAST:event_jButton1ActionPerformed

