    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int gridSize = solution.length;
        boolean correct = true;

        for (int i = 0; i < gridSize; i++) {
            for (int j = 0; j < gridSize; j++) {
                String userInput = cells[i][j].getText().trim();
                if (!userInput.equals(solution[i][j])) {
                    correct = false;
                    cells[i][j].setBackground(Color.PINK); // Optional: highlight wrong cell
                } else {
                    cells[i][j].setBackground(Color.GREEN); // Optional: highlight correct cell
                }
            }
        }
    if (correct) {
        resultLabel.setText("🎉 Congratulations! You solved the puzzle.");
        resultLabel.setForeground(new Color(0, 128, 0)); // Dark green
    } else {
        resultLabel.setText("❌ Some cells are incorrect. Try again!");
        resultLabel.setForeground(Color.RED);
    }

    }//GEN-LAST:event_jButton2ActionPerformed

